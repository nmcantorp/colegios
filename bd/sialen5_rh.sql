CREATE TABLE IF NOT EXISTS `personas` (
  `id_persona` int(15) unsigned NOT NULL AUTO_INCREMENT COMMENT 'Llave primaria de la persona',
  `doc_identidad` varchar(15) NOT NULL COMMENT 'documento de identidad de la persona',
  `fecha_nac` date NOT NULL COMMENT 'fecha de nacimiento de la persona',
  `primer_nom` varchar(20) NOT NULL COMMENT 'Primer nombre de la persona',
  `segundo_nom` varchar(20) DEFAULT NULL COMMENT 'segundo nombre de la persona',
  `primer_ape` varchar(20) NOT NULL COMMENT 'Primer apellido de la persona',
  `segundo_ape` varchar(20) DEFAULT NULL COMMENT 'Segundo apellido de la persona',
  `nombre_completo` varchar(80) DEFAULT NULL COMMENT 'Nombre completo de la persona',
  `genero` char(1) NOT NULL COMMENT 'genero de la persona, se ingresa valor (M = masculino, F = femenino)',
  `activo` char(1) DEFAULT 'S' COMMENT 'Indica si la persona esta activa en el sistema (S = si, N = no)',
  `fecha_creacion` datetime DEFAULT NULL COMMENT 'Fecha en la que se crea el registro',
  `usuario_creador` varchar(20) DEFAULT NULL COMMENT 'Usuario que crea el registro',
  `fecha_modificacion` datetime DEFAULT NULL COMMENT 'Fecha en la que se modifica el registro',
  `fecha_creador` varchar(20) DEFAULT NULL COMMENT 'Usuario que modifica el registro',
  PRIMARY KEY (`id_persona`),
  UNIQUE KEY `pers_doc_ident_uq` (`doc_identidad`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;

